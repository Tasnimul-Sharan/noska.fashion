export function slugifyCollection(collection) {
  return collection
    .toLowerCase()
    .replace(/&/g, "and")
    .replace(/[^a-z0-9]+/g, "-")
    .replace(/^-+|-+$/g, "");
}

export function getCollectionGroups() {
  return Array.from(
    products.reduce((groups, product) => {
      const current = groups.get(product.collection) || [];
      current.push(product);
      groups.set(product.collection, current);
      return groups;
    }, new Map()),
  ).map(([title, items]) => {
    const categories = [...new Set(items.map((item) => item.category))];
    const lowestPrice = Math.min(...items.map((item) => item.price));

    return {
      title,
      slug: slugifyCollection(title),
      items,
      categories,
      lowestPrice,
      image: items[0].image,
      description:
        collectionDescriptions[title] || "A refined capsule from the Noska wardrobe.",
    };
  });
}

export function getCollectionBySlug(slug) {
  return getCollectionGroups().find((collection) => collection.slug === slug);
}
